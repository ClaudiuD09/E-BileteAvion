    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String id = txtcaut.getText();
        String nume = txtnume.getText();
        String prenume = txtprenume.getText();
        String pasaport = txtpasaport.getText();
        String adresa = txtadresa.getText();
        
        
        DateFormat da = new SimpleDateFormat("yyyy-MM-dd");
        String date =da.format(txtzdn.getDate());
        
        String gen;
        
        if(r1.isSelected())
        {
            gen="Barbat";
        }
        else
        {
            gen="Femeie";
        }
        
        String contact = txttelefon.getText();
        
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            
            pst = con.prepareStatement("update client set nume = ?,prenume = ?,pasaport = ?,adresa = ?,zdn = ?,gen = ?,telefon = ? where id = ?");

            pst.setString(1, nume);
            pst.setString(2, prenume);
            pst.setString(3, pasaport);
            pst.setString(4, adresa);
            pst.setString(5, date);
            pst.setString(6, gen);
            pst.setString(7, contact);
            pst.setString(8, id);
            pst.executeUpdate();
            
            
            JOptionPane.showMessageDialog(null,"Client Acatualizat");
           
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(adaugaClienti.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(adaugaClienti.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
     
        
    }//GEN-LAST:event_jButton1ActionPerformed

