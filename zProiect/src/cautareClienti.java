    private void txtcautaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtcautaActionPerformed
        // TODO add your handling code here:
        
        String id = txtcaut.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airline","root","");
            pst=con.prepareStatement("select * from client where id =?");
            pst.setString(1,id);
            ResultSet rs = pst.executeQuery();
            
            if(rs.next() == false )
            {
                JOptionPane.showMessageDialog(this,"Nu a fost gasit");
            }
            else
            {
                String nume1 = rs.getString("nume");
                String prenume1 = rs.getString("prenume");
                String pasaport1 = rs.getString("pasaport");
                String adresa1 = rs.getString("adresa");
                
                String zdn1=rs.getString("zdn");
                Date date1= new SimpleDateFormat("yyyy-MM-dd").parse(zdn1);
                
                String gen1=rs.getString("gen");
                
                if(gen1.equals("Femeie"))
                {
                    r1.setSelected(false);
                    r2.setSelected(true);
                }
                else
                {
                    r1.setSelected(true);
                    r2.setSelected(false);
                }
                
                String contact1=rs.getString("telefon");
                
                
                txtnume.setText(nume1.trim());
                txtprenume.setText(prenume1.trim());
                txtpasaport.setText(pasaport1.trim());
                txtadresa.setText(adresa1.trim());
                txttelefon.setText(contact1.trim());
                
                txtzdn.setDate(date1);
                
                
                
            }
            
            
            
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(cautareClienti.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(cautareClienti.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(cautareClienti.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        
    }//GEN-LAST:event_txtcautaActionPerformed

    
    
    
    
    
        
        
        
        
        
        
        
        
        
        
        
        
        
        
     
    
    
    
    
    
    

